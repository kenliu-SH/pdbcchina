          <p>
            <b>pH </b> - This is an abbreviation for power/potential of hydrogen
            and is a scale used to specify the acidity or basicity of a
            solution. Acidic solutions have a lower pH value (e.g., 0-6) while
            basic solutions have higher pH values (8-14). A neutral pH is ~7,
            while the pH of human blood is slightly basic (~7.4). Depending on
            the pH of the environment, amino acids and nucleotides may be gain
            or loose protons (i.e., get protonated or deprotonated) resulting in
            the building blocks becoming charged (+ive or -ive). The charges may
            interact with each other to form ionic bonds or with ions and other
            molecules. Protonation and deprotonation of biomolecular building
            blocks may be influenced by the dielectric constant of their
            immediate neighborhoods - e.g., the ability of an amino acid on the
            surface of a protein may be different compared to that of the same
            amino acid within a hydrophobic core. The PDB does not record pH
            values for individual amino acids or nucleotides. However the pH of
            the sample solution experiment, crystallization buffers etc. may be
            recorded and can be searched in the archive.
          </p>
