          <p>
            <b>
              <a
                href="http://pdb101.rcsb.org/learn/guide-to-understanding-pdb-data/dealing-with-coordinates"
                target="_blank"
              >
                occupancy (X-ray)
              </a>
            </b>
            - A measure reported for each atomic position determined by X-ray
            crystallography. Occupancy of an atom is the fraction of molecules
            in the crystal, in which the atom is actually present in the
            position specified (model coordinates). If all molecules in a
            crystal have identical conformations, then occupancy for each atom
            is 1.00. If two or more conformations are observed for a small
            portion of the molecule in the crystal, the occupancy of the atoms
            making up that region may be less than 1.00. For example a part of
            an amino acid side chain may occupy two alternate positions so the
            occupancies may be reported as 0.5. The sum of reported occupancies
            of all distinct positions of an atom should add up to a maximum of
            1.00.
          </p>
